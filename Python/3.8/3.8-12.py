import turtle

ablak=turtle.Screen()
Eszti=turtle.Turtle()
ablak.bgcolor("green")
Eszti.shape("turtle")

for i in range(1,13):
    Eszti.penup()
    Eszti.forward(100)
    Eszti.pendown()
    Eszti.forward(10)
    Eszti.penup()
    Eszti.forward(5)
    Eszti.pendown
    Eszti.stamp()
    Eszti.penup
    Eszti.backward(115)
    Eszti.right(30)
    



ablak.mainloop()